var mongoose = require("mongoose");

//schema set up
var recipeSchema = new mongoose.Schema({
   name: String,
   image: String,
   description: String,
   author: {
      id: {
         type: mongoose.Schema.Types.ObjectId,
         ref: "User"
      },
      username: String
   },
   comments: [
      {
         type: mongoose.Schema.Types.ObjectId,
         ref: "Comment"
      }
   ]
});
//compile that into a model
module.exports = mongoose.model('Recipe', recipeSchema);
